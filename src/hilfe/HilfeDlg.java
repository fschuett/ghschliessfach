    public HilfeDlg(java.awt.Frame parent) {
        super(parent, true);
        initComponents();
        InputStream htmlF = this.getClass().getResourceAsStream(DATEI + ".html");
        if(htmlF != null) {
        	String inhalt = "";
        	Scanner inF = new Scanner(htmlF);
        	while(inF.hasNextLine())
        		inhalt += inF.nextLine();
        	this.inhalt.setText(inhalt);
        } else {
        this.inhalt.setText(fehlerText(DATEI + ".html"));
        }
        this.lKopf.setText(kopfText);
    }

	private String fehlerText(String dateiname) {
		return "<html><head><title>Hinweise nicht gefunden</title></head>"
        		+ "<body><h1>Hinweise nicht gefunden</h1>"
        		+ "<p>Die Datei mit dem Namen "
        		+ dateiname
        		+ "konnte nicht gefunden oder nicht gelesen werden. Die Hilfe ist"
        		+ " nicht verf√ºgbar.</p></body>"
        		+ "</html";
	}
	
