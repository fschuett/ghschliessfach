    public SuchDialog(java.awt.Frame parent, String[] eineListe) {
        super(parent, true);
        abgebrochen = false;
        beginn = "";
        liste = eineListe != null? eineListe : new String[]{};
        initComponents();
        suchListe.setListData(liste);
        ((AbstractDocument) suchen.getDocument()).setDocumentFilter(new DocumentFilter() {

            @Override
            public void replace(FilterBypass fb, int ofs, int len, String s, AttributeSet a) throws BadLocationException {
                beginn = suchen.getText().substring(0, ofs);
                try {
                    int index = Arrays.binarySearch(liste, beginn + s);
                    if (index < 0) {
                        index = -(index + 1);
                    }
                    if (index >= liste.length || !liste[index].startsWith(beginn + s)) {
                        index = -1;
                    }
                    if (index > -1 && index < liste.length) {
                        beginn += s;
                        suchListe.setSelectedValue(liste[index], true);
                        fb.replace(0, suchen.getText().length(), liste[index], a);
                        suchen.getCaret().setDot(liste[index].length());
                        suchen.getCaret().moveDot(beginn.length());
                    } else {
                        Toolkit.getDefaultToolkit().beep();
                    }
                } catch (BadLocationException ex) {
                    Logger.getLogger(SuchDialog.class.getName()).log(Level.SEVERE, null, ex);
                }


            }
        });
        suchen.requestFocusInWindow();
    }

    public void setListe(String[] liste) {
        this.liste = liste;
        suchen.setText("");
        suchListe.setListData(liste);
        beginn = "";
        abgebrochen = false;
    }

    public boolean istAbgebrochen() {
        return abgebrochen;
    }

    public String getResult() {
        return (String) suchListe.getSelectedValue();
    }

